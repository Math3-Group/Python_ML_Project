# Librer铆as
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

# Texto y preprocesamiento
import re
import string
import nltk
from nltk.corpus import stopwords

# Machine learning
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import LabelEncoder
from sklearn.feature_extraction.text import TfidfVectorizer

# M茅tricas
from sklearn.metrics import classification_report, confusion_matrix

# Keras
import tensorflow as tf
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Dense, Dropout
from tensorflow.keras.utils import to_categorical

sns.set(style="darkgrid")
nltk.download('stopwords')

#Carga de datos

df = pd.read_csv("mbti_1.csv")

#Limpieza de datos

def limpiar_texto(texto):
    texto = texto.lower()
    texto = re.sub(r'[^a-z\s]', '', texto)
    texto = re.sub(r'\s+', ' ', texto).strip()
    return texto
df['clean_posts'] = df['posts'].apply(limpiar_texto)

# Vectorizacion con TF-IDF
vectorizador = TfidfVectorizer(max_features=5000)
X = vectorizador.fit_transform(df['clean_posts'])

# Codificaci贸n de etiquetas
le = LabelEncoder()
y = le.fit_transform(df['type'])  # y va de 0 a 15
y_cat = to_categorical(y)

print(f"Clases MBTI detectadas: {list(le.classes_)}")

# Dividisi贸n de datos
X_train, X_test, y_train_cat, y_test_cat = train_test_split(
    X, y_cat, test_size=0.2, random_state=42, stratify=y)

#Definici贸n del modelo

def crear_modelo(input_dim, output_dim):
    modelo = Sequential([
        Dense(128, input_shape=(input_dim,), activation='relu'),
        Dropout(0.3),
        Dense(64, activation='relu'),
        Dropout(0.3),
        Dense(output_dim, activation='softmax')
    ])
    modelo.compile(optimizer='adam',
                   loss='categorical_crossentropy',
                   metrics=['accuracy'])
    return modelo

modelo = crear_modelo(X_train.shape[1], y_cat.shape[1])

#Entrenamiento
historial = modelo.fit(
    X_train.toarray(), y_train_cat,
    validation_data=(X_test.toarray(), y_test_cat),
    epochs=20,
    batch_size=64,
    verbose=1
)

#Evaluaci贸n
loss, acc = modelo.evaluate(X_test.toarray(), y_test_cat, verbose=0)
print(f"\n Evaluaci贸n en test:\nP茅rdida: {loss:.4f}\nPrecisi贸n: {acc:.4f}")

#Visualizaci贸n
def plot_metricas(historial):
    fig, axs = plt.subplots(1, 2, figsize=(14, 5))

    # P茅rdida
    axs[0].plot(historial.history['loss'], label='Entrenamiento')
    axs[0].plot(historial.history['val_loss'], label='Validaci贸n')
    axs[0].set_title('P茅rdida durante el entrenamiento')
    axs[0].set_xlabel('pocas')
    axs[0].set_ylabel('P茅rdida')
    axs[0].legend()

    # Precisi贸n
    axs[1].plot(historial.history['accuracy'], label='Entrenamiento')
    axs[1].plot(historial.history['val_accuracy'], label='Validaci贸n')
    axs[1].set_title('Precisi贸n durante el entrenamiento')
    axs[1].set_xlabel('pocas')
    axs[1].set_ylabel('Precisi贸n')
    axs[1].legend()

    plt.tight_layout()
    plt.show()

plot_metricas(historial)